package com.example.securitycourse.audit;

/**
 * Категории событий аудита.
 * Используется для группировки на дашборде, фильтрации и применения правил корреляции.
 */
public enum AuditCategory {

    /** Аутентификация: логин, логаут, MFA, смена пароля. */
    AUTH,

    /** Авторизация: попытки доступа, отказы. */
    ACCESS,

    /** Управление пользователями: CRUD, изменение ролей. */
    USER_MGMT,

    /** Доступ к данным и операции с ними (экспорт, удаление, чтение чувствительных данных). */
    DATA,

    /** Изменение конфигурации системы и правил корреляции. */
    CONFIG,

    /** Сетевые события: подозрительные IP, гео-аномалии, нерабочее время. */
    NETWORK,

    /** Инциденты, сгенерированные движком корреляции. */
    INCIDENT,

    /** Системные события: запуск/остановка сервисов, нарушение целостности журнала. */
    SYSTEM
}