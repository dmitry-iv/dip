package com.example.securitycourse.audit;

/**
 * Перечень всех возможных действий, записываемых в журнал аудита.
 */
public enum AuditActions {

    /** Успешный вход в систему. */
    LOGIN_SUCCESS,

    /** Неудачная попытка входа. */
    LOGIN_FAILURE,

    /** Выход из системы. */
    LOGOUT,

    /** Регистрация нового пользователя. */
    REGISTER,

    /** Создание пользователя администратором. */
    USER_CREATED,

    /** Изменение данных пользователя. */
    USER_UPDATED,

    /** Удаление пользователя. */
    USER_DELETED,

    /** Блокировка учётной записи. */
    USER_LOCKED,

    /** Разблокировка учётной записи. */
    USER_UNLOCKED,

    /** Изменение ролей пользователя. */
    ROLES_CHANGED,

    /** Смена пароля. */
    PASSWORD_CHANGED,

    /** Доступ запрещён (попытка доступа к чужому ресурсу). */
    ACCESS_DENIED
}